/**
题目描述：现需要在某城市进行5G网络建设，已经选取N个地点设置5G基站，编号固定为1到N，接下来需要各个基站之间使用光纤进行连接以确保基站能互联互通，不同
基站之间架设光纤的成本各不相同，且有些节点之间已经存在光纤相连，请你设计算法，计算出能联通这些基站的最小成本是多少。
注意：基站的联通具有传递性，入基站A与基站B架设了光纤，基站B与基站C也架设了光纤，则基站A与基站C视为可以互相联通
输入描述：第一行输入表示基站的个数N，其中0<N<=20
第二行输入表示具备光纤直连条件的基站对的数目M，其中0<M<N*(N-1)/2
从第三行开始连续输入M行数据，格式为 X Y Z P，其中X Y表示基站的编号，0<X<=N，0<Y<=N且x不等于y，Z表示在X Y之间架设光纤的成本，其中
0<Z<100，P表示是否已存在光纤连接，0表示未连接，1表示已连接
输出描述：如果给定条件，可以建设成功互联互通的5G网络，则输出最小的建设成本；
如果给定条件，无法建设成功互联互通的5G网络，则输出-1
 */

/**
 * 解题思路
 * 这个问题是一个典型的 最小生成树（MST）问题。基站之间的光纤连接需要选择性地添加以确保整个网络的互联互通，且要求总成本最小。
理解问题：

我们可以把每个基站看成图中的一个节点，基站之间可以通过光纤连接，这些连接有不同的成本。
题目中给定了某些基站对之间已经有光纤连接（成本为 0 或 1），这些是已经存在的边。
需要根据现有的连接信息计算最小的额外连接成本以确保所有基站能互相联通。
图的表示：

使用图的 邻接矩阵 来表示基站之间的连接情况。矩阵的每个元素表示两个基站之间的连接成本。
如果两个基站之间已经有光纤连接，则直接用给定的成本（1 或 0），否则用一个非常大的值表示不可达。
最小生成树（MST）：

可以使用 Prim 算法 或 Kruskal 算法 来求解最小生成树。
由于基站的个数 N <= 20，可以选择用 Prim 算法，其时间复杂度是 O(N^2)，在此限制下非常合适。
问题的特殊情况：

如果某些基站对之间无法通过任何光纤连接，即使考虑额外的光纤连接，整个图仍然无法联通，应该返回 -1。
 */
